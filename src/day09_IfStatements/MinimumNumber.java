package day09_IfStatements;

public class MinimumNumber {

    public static void main(String[] args) {
        int n1= 100, n2= 14;

        if(n1<n2){
            System.out.println(n1+ " is the minimum number");
        }else{
            System.out.println(n2+ " is the minimum number");
        }
    }
}
