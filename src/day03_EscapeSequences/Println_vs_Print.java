package day03_EscapeSequences; //Package name of the class

public class Println_vs_Print {

    public static void main(String[] args) { //main method

        System.out.println("Hello Cydeo"); //prints Hello Cydeo to console, then appends a new line
        System.out.println("How are you today?");

        System.out.print("Hello Cydeo");  //prints Hello Cydeo to console, without appending a new line
        System.out.println("How are you today?");

        System.out.print("one two three");
        System.out.print("four five six");
        System.out.println("seven eight nine");
        System.out.print("ten eleven twelve");


    }
}
